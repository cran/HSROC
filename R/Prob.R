function (t1, t2, pi, S1, S2, C1, C2) 
{
    p = pi * S1^(t1) * (1 - S1)^(1 - t1) * S2^(t2) * (1 - S2)^(1 - 
        t2)/(pi * S1^(t1) * (1 - S1)^(1 - t1) * S2^(t2) * (1 - 
        S2)^(1 - t2) + (1 - pi) * C1^(1 - t1) * (1 - C1)^(t1) * 
        C2^(1 - t2) * (1 - C2)^t2)
    return(p)
}
